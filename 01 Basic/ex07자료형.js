// 1. number : 정수, 실수 등 산술 연산이 가능한 자료형
let num1 = 1.234;
let num2 = 50;
console.log(num1);
console.log(num2);

// 2. string : 문자열로 이루어진 자료형
let str1 = '서무광';
let str2 = '15';
 console.log(str1);
 console.log(str2);


 // 3. boolean : 참/거짓으로 표현되는 논리 형태의 자료형
 let isFemale = true;
 let isRich = false;
 console.log(isFemale);
 console.log(isRich);

 // 4. undefined : 값이 할당된 적이 없거나 존재하지 않는 속성일 때의 유형
 let vall;
 console.log(vall);

 // 5. null : 아무런 값을 나타내지 않을 떄의 자료형
 let val2 = null;
 console.log(val2);